#include <iostream>
#include<string.h>
#include<stdio.h>
using namespace std;
int main() 
{
int n,j;
j=2*n-1;
unsigned long long int answer;
char A[3000];
answer=1;
cin>>n;  //size of array
cin>>A;  //get integers
const unsigned int k=1000000007;
for (int i=0;i<=j;i++)
{
	answer=(answer*A[i]) % k;
}
cout<<answer;
}
